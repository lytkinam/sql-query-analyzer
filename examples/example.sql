// Пример пакетного 1C-запроса
// Шаг 1: отбираем активных сотрудников
ВЫБРАТЬ
    Сотрудники.Ссылка КАК Сотрудник,
    Сотрудники.Наименование
ПОМЕСТИТЬ ВТ_Сотрудники
ИЗ
    Справочник.Сотрудники КАК Сотрудники
ГДЕ
    Сотрудники.Уволен = ЛОЖЬ
;

// Шаг 2: подтягиваем начисления через LEFT JOIN
ВЫБРАТЬ
    ВТ_Сотрудники.Сотрудник,
    НачисленияДвижения.Сумма
ПОМЕСТИТЬ ВТ_Начисления
ИЗ
    ВТ_Сотрудники КАК ВТ_Сотрудники
    ЛЕВОЕ СОЕДИНЕНИЕ РегистрНакопления.Начисления КАК НачисленияДвижения
        ПО ВТ_Сотрудники.Сотрудник = НачисленияДвижения.Сотрудник
;

// Шаг 3: итоговый результат с группировкой
ВЫБРАТЬ
    ВТ_Начисления.Сотрудник,
    СУММА(ВТ_Начисления.Сумма) КАК ИтогоСумма
ИЗ
    ВТ_Начисления КАК ВТ_Начисления
СГРУППИРОВАТЬ ПО
    ВТ_Начисления.Сотрудник
;

// Очистка
УНИЧТОЖИТЬ ВТ_Сотрудники;
УНИЧТОЖИТЬ ВТ_Начисления;
